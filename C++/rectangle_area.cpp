#include <iostream>
using namespace std;
int main(){
    //To calculate the area of a rectangle you have three variables
    //The length and the width of the rectangle and the area of the rectangle
    //Common formula for area of a rectangle is L * W
    int RectangleWidth, RectangleLength, RectangleArea;
    RectangleWidth = 3;
    RectangleLength = 9;
    RectangleArea = RectangleWidth * RectangleLength;
    cout <<"The lenght of the rectangle is " << RectangleLength<< "\n";
    cout << "The width of the rectangle is " << RectangleWidth << "\n";
    cout << "The area of this Rectangle is " << RectangleArea;
    return 0;
}
