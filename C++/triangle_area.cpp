#include <iostream>
using namespace std;
int main(){
//This is a simple code to calculate the area of a triangle
//Formula for area of a triangle is 1/2 base * height
//So we have two variables the base and the height
    int TriangleBase, TriangleHeight;
    float TriangleArea;
    TriangleBase = 4;
    TriangleHeight = 8;
    TriangleArea = TriangleHeight*0.5* TriangleBase;
    cout << "The area of the triangle is " << TriangleArea;
    return 0;
}

